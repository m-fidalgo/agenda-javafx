package agenda.entities;

public class Contact {
  private String nome;
  private int idade;
  private String tel;

  public String getNome() {
    return nome;
  }

  public void setNome(String nome) {
    this.nome = nome;
  }

  public int getIdade() {
    return idade;
  }

  public void setIdade(int idade) {
    this.idade = idade;
  }

  public String getTel() {
    return tel;
  }

  public void setTel(String tel){
    this.tel = tel;
  }
}
